package com.gte619n.healthfitness.core.medication;

/**
 * Physical form of the drug, used for image generation and display.
 */
public enum DrugForm {
    INJECTABLE_VIAL,    // Glass vial with rubber stopper
    TABLET,             // Solid tablet/pill
    CAPSULE,            // Hard shell capsule
    SOFTGEL,            // Soft gelatin capsule
    CREAM,              // Topical cream or gel
    PATCH,              // Transdermal patch
    LIQUID,             // Oral liquid/syrup
    POWDER              // Powder for mixing
}
