package com.gte619n.healthfitness.core.medication;

/**
 * Classification of drug types for categorization and display.
 */
public enum DrugCategory {
    PRESCRIPTION,   // Rx medications requiring a prescription
    SUPPLEMENT,     // Vitamins, minerals, dietary supplements
    OTC,            // Over-the-counter medications
    PEPTIDE,        // Research peptides (BPC-157, TB-500, etc.)
    TOPICAL         // Creams, gels, patches
}
